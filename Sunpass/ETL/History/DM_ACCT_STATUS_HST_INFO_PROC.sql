set serveroutput on
set verify on
set echo on

CREATE OR REPLACE PROCEDURE DM_ACCT_STATUS_HST_INFO_PROC IS

TYPE DM_ACCT_STATUS_HST_INFO_TYP IS TABLE OF DM_ACCT_STATUS_HST_INFO%ROWTYPE 
     INDEX BY BINARY_INTEGER;
DM_ACCT_STATUS_HST_INFO_tab DM_ACCT_STATUS_HST_INFO_TYP;


P_ARRAY_SIZE NUMBER:=10000;


CURSOR C1 IS SELECT 
    PREV_STATUS PREV_STATUS
    ,NEW_STATUS NEW_STATUS
    ,STAT_CHANGE_DATE STAT_CHANGE_DATE
    ,STAT_POST_DATE STAT_POST_DATE
    ,EMP_ID EMP_ID
    ,ACCT_NUM ACCT_NUM
    ,'SUNPASS' SOURCE_SYSTEM
FROM PA_ACCT_STATUS_HISTORY;

BEGIN
 
  OPEN C1;  

  LOOP

    /*Bulk select */
    FETCH C1 BULK COLLECT INTO DM_ACCT_STATUS_HST_INFO_tab
    LIMIT P_ARRAY_SIZE;


    /*ETL SECTION BEGIN */

    /*ETL SECTION END   */

    /*Bulk insert */ 
    FORALL i in DM_ACCT_STATUS_HST_INFO_tab.first .. DM_ACCT_STATUS_HST_INFO_tab.last
           INSERT INTO DM_ACCT_STATUS_HST_INFO VALUES DM_ACCT_STATUS_HST_INFO_tab(i);
                       
    EXIT WHEN C1%NOTFOUND;
  END LOOP;

  COMMIT;

  CLOSE C1;

  COMMIT;

  EXCEPTION
  WHEN OTHERS THEN
     DBMS_OUTPUT.PUT_LINE('ERROR CODE: '||SQLCODE);
     DBMS_OUTPUT.PUT_LINE('ERROR MSG: '||SQLERRM);
END;
